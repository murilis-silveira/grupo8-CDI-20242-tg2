     public void readJTable()
    {
         DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
         
        modelo.setNumRows(0);
         
         ConsultasDAO fdao = new ConsultasDAO();
         
         for(Consultas f: fdao.readVendaCliente())
         {
             modelo.addRow(new Object[]
             {
                f.getCliente(), 
                f.getVenda(),
                f.getTotalGasto()
                              
               } );         
         }         
         
    }
