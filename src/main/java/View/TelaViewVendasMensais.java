      public void readJTable()
   {
         DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
         
        modelo.setNumRows(0);
         
         ViewVendasMensaisDAO fdao = new ViewVendasMensaisDAO();
         
         for(ViewVendasMensais f: fdao.read())
         {
             modelo.addRow(new Object[]
             {
                 f.getCodFilial(),
                 f.getTotalVenda(),
                 f.getTotalValor()

             } );         
         
         }         
         
    }
    
    
    
    
    
    
